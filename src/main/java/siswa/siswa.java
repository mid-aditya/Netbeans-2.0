    public void bersih() {
        txtNim.setText(""); // Mengosongkan text field NIM
        txtNama.setText(""); // Mengosongkan text field Nama
        txtCari.setText(""); // Mengosongkan text field Cari
        cboJurusan.setSelectedIndex(0); // Mengatur combo box ke item pertama (index 0)
    }

    public void tampildata() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("NIM");
        tbl.addColumn("Nama");
        tbl.addColumn("Jurusan");
        tblSiswa.setModel(tbl); // Mengatur model untuk JTable tblMahasiswa

        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM siswa");

            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("nim"), // Mengambil data NIM dari ResultSet
                    res.getString("nama"), // Mengambil data Nama dari ResultSet
                    res.getString("jurusan") // Mengambil data Jurusan dari ResultSet
                });
            }
            tblSiswa.setModel(tbl); // Mengatur model untuk JTable tblMahasiswa setelah data ditambahkan
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Terjadi kesalahan saat mengambil data: " + e.getMessage());
        }
    }


