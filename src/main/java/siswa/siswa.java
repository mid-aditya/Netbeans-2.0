    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        try (Connection connection = koneksi.GetConnection(); PreparedStatement statement = connection.prepareStatement("INSERT INTO siswa (nim, nama, jurusan) VALUES (?, ?, ?)")) {

            // Set the parameters for the PreparedStatement
            statement.setString(1, txtNim.getText());
            statement.setString(2, txtNama.getText());
            statement.setString(3, (String) cboJurusan.getSelectedItem());

            // Execute the update
            int rowsAffected = statement.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                tampildata();
                bersih();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e.getMessage());
        }

    }//GEN-LAST:event_btSimpanActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        // TODO add your handling code here:
         // Ambil data dari field input
    String nim = txtNim.getText();
    String nama = txtNama.getText();
    String jurusan = (String) cboJurusan.getSelectedItem();

    // Validasi input
    if (nim.isEmpty()) {
        JOptionPane.showMessageDialog(null, "NIM tidak boleh kosong");
        return;
    }

    // Proses update data di database
    try (Connection connection = koneksi.GetConnection();
         PreparedStatement statement = connection.prepareStatement(
            "UPDATE siswa SET nama = ?, jurusan = ? WHERE nim = ?")) {

        // Set parameter untuk PreparedStatement
        statement.setString(1, nama);
        statement.setString(2, jurusan);
        statement.setString(3, nim);

        // Eksekusi update
        int rowsAffected = statement.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil diperbarui");
            tampildata();  // Menampilkan data yang diperbarui di JTable
            bersih();  // Mengosongkan field input
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan untuk NIM: " + nim);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal memperbarui data: " + e.getMessage());
    }

    }//GEN-LAST:event_btEditActionPerformed

